# Linked list is a form of sequential collection and it doen not have to be in order. A linked list is made
# up of independent nodes that may contain any type of data and each node has a reference to the next node in
# the link. e.g. train, it has engine and cars(nodes) and the engines and cars are linked via
# hand_hooks(links). The engine is head and the last cart is tail.
# Each car is independent
# Cars: Passengers and links(nodes: data and link)
# Head(001)-->1(111)-->2(222)-->4(333)-->5(null)<--Tail
#             001      111      222      333
# The first component of linked list is head which has a reference to first element in list and no self-reference
# Then we have nodes which consists of 2 parts first part is value of node and 2nd part is reference to next node

