# range() is used to generate a sequence of integers starting from 0 by default and increments by 1 till
# n -1. syntax: range(start, stop, stepsize)
# start: Starting position, if we don't give anything it considers 0
# stop: Ending position, the range of integers stops one element prior to the stop if stop is n then it will
# stop at n-1
# step size: Increment by step size, if we don't mention by default its 1.
# syntax: range(i,j) --> i, i+1, i+2...j-1
# e.g. range(1,10) --> 1, 1+1, 1+2, 1+3....10-1 so range will become 1,2,3,4,5,6,7,8,9
