# Formal Arguments: Function definition parameters are called as formal arguments
# Actual Arguments: Function call arguments are called as formal arguments
# e.g.

def add(x, y):  # here x and y are are formal arguments
    z = x + y
    print(z)


add(10, 20)  # here 10 and 20 are actual arguments

# There are different types of Actual Arguments
# Positional Arguments
# Keyword Arguments
# Default Arguments
# Variable Length Arguments
# Keyword Variable Length Arguments
